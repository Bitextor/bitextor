<td align="right">Karl Heinrich Marx (May 5, 1818 March 14, 1883)
was a German[1] philosopher, political economist, historian,
sociologist, humanist, political theorist, and revolutionary
credited as the founder of communism. Marx summarized his approach
to history and politics in the opening line of the first chapter of
The Communist Manifesto (1848): The history of all hitherto
existing society is the history of class struggles. Marx argued
that capitalism, like previous socioeconomic systems, will produce
internal tensions which will lead to its destruction Just as
capitalism replaced feudalism, socialism will in its turn replace
capitalism and lead to a stateless, classless society which will
emerge after a transitional period, the dictatorship of the
proletariat.</td>
<td align="rignth">On the other hand, Marx argued that
socio-economic change occurred through organized revolutionary
action. He argued that capitalism will end through the organized
actions of an international working class, led by a Communist
Party: Communism is for us not a state of affairs which is to be
established, an ideal to which reality will have to adjust itself.
We call communism the real movement which abolishes the present
state of things. The conditions of this movement result from the
premises now in existence. (from The German Ideology)</td>
